        timestamp: news.timestamp || 0  // –º–æ–∂–Ω–æ —Å–æ—Ö—Ä–∞–Ω–∏—Ç—å, –µ—Å–ª–∏ –Ω–∞–¥–æ —Å–æ—Ä—Ç–∏—Ä–æ–≤–∞—Ç—å
